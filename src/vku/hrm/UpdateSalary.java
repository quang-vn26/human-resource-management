    private void update_search(){
        try {
            System.out.println("txt_search:"+txt_searh_id.getText());
            String sql ="select * from Employee_information where id=? ";
            pst=conn.prepareStatement(sql);
            pst.setString(1,txt_searh_id.getText());
//            System.out.println("vku.hrm.UpdateSalary.txt_searh_idKeyReleased() text_search_id:"+txt_searh_id.getText())        ;
            rs=pst.executeQuery();
            
               String add1 =rs.getString("id");
                txt_empid.setText(add1);

                String add2 =rs.getString("first_name");
                txt_firstname.setText(add2);

                String add3 =rs.getString("surname");
                txt_surname.setText(add3);

                String add4 =rs.getString("Dob");
                txt_dob.setText(add4);

                String add5 =rs.getString("Salary");
                txt_salary.setText(add5);

                String add8 =rs.getString("Department");
                txt_dept.setText(add8);
        } catch (Exception e) {
           // JOptionPane.showConfirmDialog(null, e);
           JOptionPane.showMessageDialog(null, "Không có dữ liệu");
        }
        finally{
            try {
                rs.close();
                pst.close();
            } catch (Exception e) {
            }
        }
    }
