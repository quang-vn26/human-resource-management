    private void updateSalaryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateSalaryActionPerformed
        // TODO add your handling code here:
       int p = JOptionPane.showConfirmDialog(null, "Bạn có muốn cập nhật lương nhân viên?","Cập nhật",JOptionPane.YES_NO_OPTION);
       if(p==0){
            int salary = Integer.parseInt(txt_salary.getText());
            if(r_percentage.isSelected()== true){
                int getPercentage = Integer.parseInt(txt_percentage.getText());
                int calcPercentage = salary /100 * getPercentage + salary;
                String xP = String.valueOf(calcPercentage);
                txt_salary.setText(xP);
            }
            else if(r_amount.isSelected()==true){
                int getAmt = Integer.parseInt(txt_amount.getText());
                int calcAmount = salary + getAmt;
                String xA = String.valueOf(calcAmount);
                txt_salary.setText(xA);
                }
            }
            
        try {
            String txt1 = txt_empid.getText();
            String txt2 = txt_salary.getText();
            
            String sql = "update Employee_information set id ='"+txt1+"',Salary = '"+txt2+"' where id ='"+txt1+"'";
            pst = conn.prepareStatement(sql);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null,"Cập nhật lương thành công");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
          finally{
                try {
                    rs.close();
                    pst.close();
                } catch (Exception e) {
                    JOptionPane.showConfirmDialog(null, e);
                }
            }
          
    }//GEN-LAST:event_updateSalaryActionPerformed

